def celsius_to_fahrenheit(self):
    celsius = (self*9/5)+32
    return celsius

def fahrenheit_to_celsius(self):
    fahrenheit = (self-32)*5/9
    return fahrenheit


def kelvin_to_celsius(self):
    kelvin = self - 273.15
    return kelvin

def celsius_to_kelvin(self):
    kelvin = self + 273.15
    return kelvin
